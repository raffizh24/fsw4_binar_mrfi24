const day = "MONDAY";
switch(day){
    case "MONDAY": 
        console.log("Hari Senin");
        break;
    case "TUESDAY": 
        console.log("Hari Selasa");
        break;
    case "WEDNESDAY": 
        console.log("Hari Rabu");
        break;
    case "THURSDAY": 
        console.log("Hari Kamis");
        break;
    case "FRIDAY": 
        console.log("Hari Jumat");
        break;
    case "SATURDAY": 
        console.log("Hari Sabtu");
        break;
    case "SUNDAY": 
        console.log("Hari Minggu");
        break;
    default:
        console.log("Hari tidak tersedia");
}