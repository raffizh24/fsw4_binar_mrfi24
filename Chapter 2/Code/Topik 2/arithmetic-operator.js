console.log("<-- Arithmetic Operator -->");

const p = 5;
const q = 10;
const r = '100';
let s = 1;

// Add 
console.log(p + q);

// Add and multiply
console.log((p + q) * r);

// Reminder / Modulus
console.log(p % r);

// Increment
console.log('Postfix :',s++);
console.log('Prefix :',++s);

// Decrement
console.log('Postfix :',s--);
console.log('Prefix :',--s);

// Unary negation
console.log(-s);

// Unary plus
console.log(+s);

// Exponentiation
console.log(p ** 2);

// Division
console.log(p / r);
